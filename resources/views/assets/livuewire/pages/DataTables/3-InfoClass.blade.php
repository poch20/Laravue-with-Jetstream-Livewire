<h1 class="page_title">Bootstrap 3</h1>
<div class="info">
  <p>DataTables can integrate seamlessly with <a href="http://getbootstrap.com/">Bootstrap 3</a> using Bootstrap's <a href=
  "http://getbootstrap.com/css/#tables">table styling options</a> to present an interface with a uniform design, based on Bootstrap, for your site / app.</p>
  <p>DataTables' Bootstrap integration also provides a renderer for the pagination control in DataTables to ensure that the pagination of the table is also
  styled consistently by Bootstrap.</p>
  <p>This integration is done simply by including the DataTables Bootstrap files (CSS and JS) which sets the defaults needed for DataTables to be initialised as
  normal, as shown in this example.</p>
</div>
